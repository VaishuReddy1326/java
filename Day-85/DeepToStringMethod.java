//deepToString() method is used to print all the values in 2D-array
import java.util.Arrays;

public class DeepToStringMethod {
    public static void main(String[] args) {
        int arr[][]= {
            {3,5,6,7,7},
            {2,4,6,8,9},
            {3,4,5,6,8}
        };
        System.out.println(Arrays.deepToString(arr));

       
    }
    
}
