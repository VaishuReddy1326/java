import static java.lang.Math.*;
public class StaticImport {
    public static void main(String[] args) {

        System.out.println(abs(-50));

        System.out.println(ceil(3.5));

        System.out.println(round(20.4));

        System.out.println(sqrt(7));

        System.out.println(pow(3,6));

        System.out.println(min(10,40));

        System.out.println(max(43,89));
    }
}