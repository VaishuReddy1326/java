

public class Assignment_Operator {
    public static void main(String[] args){
        int a=25,b=40,c=4,d=8;
        System.out.println(a=b);
        System.out.println(a+=b);
        System.out.println(d-=b);
        System.out.println(a*=b);
        System.out.println(c%=b);
        System.out.println(a/=b);
        System.out.println(b<<=d);
        System.out.println(c>>=a);

    }
    
}
