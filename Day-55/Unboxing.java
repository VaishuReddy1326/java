//Automatic converson of wrapper class object to its primitive data types

public class Unboxing {
    public static void main(String[] args) {
        Integer num = 15;

        int num1 = num;

        System.out.println(num1);

        Float f = 10.05f;

        float f1 = f;

        System.out.println(f1);

        Byte b = 12;

        byte b1 = b;

        System.out.println(b1);

        Character ch = 'b';

        char ch1 = ch;

        System.out.println(ch1);

    }
}
