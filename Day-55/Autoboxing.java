//it automatically converts the primitive datatypes to object of their corresponding wrapper classes

public class Autoboxing {
    public static void main(String[] args) {

        float f = 10.5f;

        Float f1 = f;  //converting primitive type to wrapper class object

        System.out.print(f1);

        int a = 10;

        Integer b = a;

        System.out.print(b);
    }
    
}
